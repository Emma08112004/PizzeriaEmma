package Entities;

public class Livreur
{

    private int numLiv;
    private String nomLiv;
    public Livreur(int numLiv, String nomLiv) {
        this.numLiv = numLiv;
        this.nomLiv = nomLiv;

    }

    public int getNumLiv() {
        return numLiv;
    }

    public String getNomLiv() {
        return nomLiv;
    }


}
